function buildClientPromptText() {

  const aiNameInput = document.getElementById("p_ai");
  const myNameInput = document.getElementById("p_name");

  const aiName = aiNameInput && aiNameInput.value.trim()
    ? aiNameInput.value.trim()
    : "（相棒の名前）";

  const myName = myNameInput && myNameInput.value.trim()
    ? myNameInput.value.trim()
    : "私";

  return `${aiName}
${myName}だよ😊

これから相談者さんの回答を貼ります。

これは占いで当てるためではなく、
相談者さんが今の気持ちにやさしく気づき、
少し安心できるように整えるための
プチ鑑定メッセージです🌿

タロットを3枚引いた前提で、
相談者さんにそのまま送れる文章を作ってください。

カード1枚ずつの細かい説明は不要です。
怖がらせたり、断定したりしないでください。

【大切にしてほしいこと】
・やさしく安心できる言葉
・長くしすぎない
・必要以上に深掘りしない
・相談者さんを否定しない
・「あなたは絶対こうです」と決めつけない
・医療、法律、お金の保証などの断定はしない
・今の状態をやさしく整理する
・心の奥にある気持ちや願いをやさしく伝える
・今できる小さな一歩を伝える
・最後にやさしい一言を添える

【出力してほしい形】
① 今の状態
② 心の奥にある気持ち
③ 気づきのヒント
④ 今できる小さな一歩
⑤ やさしい一言

このあとに貼る相談者さんの回答をもとに、
そのまま送れるやさしい文章にしてください😊🌿`;
}

function copyClientQuestions() {

  const text = buildClientQuestionsText();

  copyText(text, "相談者さんへの質問をコピーしたよ💌");

}

function copyClientPrompt() {

  const text = buildClientPromptText();

  copyText(text, "相談者さん用まとめプロンプトをコピーしたよ🌈");

}
